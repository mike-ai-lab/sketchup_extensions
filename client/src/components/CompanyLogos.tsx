export function CompanyLogo({ name, className = "" }: LogoProps) {
  const logoUrl = LOGO_URLS[name.toLowerCase()];
  
  if (logoUrl) {
    return (
      <img 
        src={logoUrl} 
        alt={`${name} logo`} 
        className={`h-6 w-auto ${className}`}
      />
    );
  }
  
  // Fallback to text for logos not available
  return (
    <span className={`font-black uppercase tracking-wider ${className}`}>
      {name}
    </span>
  );
}

